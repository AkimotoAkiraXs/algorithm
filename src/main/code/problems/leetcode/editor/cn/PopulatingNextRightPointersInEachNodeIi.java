        Node nextStart, pre;

        public Node connect(Node root) {
            if (root == null) {
                return null;
            }
            Node start = root;
            while (start != null) {
                pre = nextStart = null;
                for (Node node = start; node != null; node = node.next) {
                    if (node.left != null) {
                        change(node.left);
                    }
                    if (node.right != null) {
                        change(node.right);
                    }
                }
                start = nextStart;
            }
            return root;
        }

        public void change(Node node) {
            if (pre != null) {
                pre.next = node;
            }
            if (nextStart == null) {
                nextStart = node;
            }
            pre = node;
        }
